import os
# os.system() is used to run the command in the command prompt.
os.system("echo 'Hello World'") # print Hello World
os.system("cls") # clear the screen
os.system("dir") # list the files in the current directory
os.system("python --version") # check the python version
os.system("ipconfig") # check the ip address
os.system("date") # check the date
os.system("notepad") # open the notepad
os.system("write") # open the write application
